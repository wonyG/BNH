package household;

public class Household {
	private int householdID; 
	private int householdInOut;
	private String userID;
	private String householdDate;
	private String householdContent;
	private int householdPrice;
	private int householdAvailable;
	
	public int getHouseholdID() {
		return householdID;
	}
	public void setHouseholdID(int householdID) {
		this.householdID = householdID;
	}
	public int getHouseholdInOut() {
		return householdInOut;
	}
	public void setHouseholdInOut(int householdInOut) {
		this.householdInOut = householdInOut;
	}
	public String getUserID() {
		return userID;
	}
	public void setUserID(String userID) {
		this.userID = userID;
	}
	public String getHouseholdDate() {
		return householdDate;
	}
	public void setHouseholdDate(String householdDate) {
		this.householdDate = householdDate;
	}
	public String getHouseholdContent() {
		return householdContent;
	}
	public void setHouseholdContent(String householdContent) {
		this.householdContent = householdContent;
	}
	public int getHouseholdPrice() {
		return householdPrice;
	}
	public void setHouseholdPrice(int householdPrice) {
		this.householdPrice = householdPrice;
	}
	public int getHouseholdAvailable() {
		return householdAvailable;
	}
	public void setHouseholdAvailable(int householdAvailable) {
		this.householdAvailable = householdAvailable;
	}
	
}
